#include "../ft_printf_outils.h"

int	is_specifier(char c)
{
	if (
		c == 'c'
		|| c == 's'
		|| c == 'p'
		|| c == 'd'
		|| c == 'i'
		|| c == 'u'
		|| c == 'x'
		|| c == 'X'
		|| c == '%')
		return (1);
	return (0);
}
